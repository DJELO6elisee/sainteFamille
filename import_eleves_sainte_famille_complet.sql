INSERT IGNORE INTO enrollments (student_id, class_id, enrollment_date, status, school_year)
SELECT s.id, c.id, COALESCE(e.enrollment_date, CURDATE()), 'active', '2025-2026'
FROM _staging_import e
JOIN students s ON s.student_code = e.student_code
JOIN classes c ON c.name = e.class_name AND c.school_year = '2025-2026';

