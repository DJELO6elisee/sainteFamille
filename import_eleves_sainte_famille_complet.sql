SET @next_sfe = (SELECT COALESCE(MAX(CAST(SUBSTRING(registration_number, 4) AS UNSIGNED)), 0) FROM students WHERE registration_number LIKE 'SFE%');

INSERT INTO students (first_name, last_name, student_code, registration_number, registration_mode)
SELECT first_name, last_name, student_code, CONCAT('SFE', LPAD(@next_sfe := @next_sfe + 1, 3, '0')), 'onsite'
FROM _staging_import
ORDER BY student_code
ON DUPLICATE KEY UPDATE first_name = VALUES(first_name), last_name = VALUES(last_name);

